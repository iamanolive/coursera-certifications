# iterating over sequence
for i in [0, 1, 2, 3, 4]:
    print(i)

# using range to define list
for i in range(5):
    print(i)
# defining start and end values
for i in range(1, 5):
    print(i)
# counting with steps of 2
for i in range(1, 10, 2):
    print(i)


print(range(5)) # generates iterable
print(list(range(5)))
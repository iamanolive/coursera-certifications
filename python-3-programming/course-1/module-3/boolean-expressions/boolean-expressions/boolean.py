# boolean expressions
print(5 == 5) # is equal to operator
print(5 == 6) # is equal to operator
print(5 != 6) # not equal to operator

# comparison operators
print(3 < 4) # less than operator
print(4 <= 4) # less than equal to
print(5 > 6) # greater than operator
print(7 >= 8) # greater than equal to